package com.leleliu008.androidsdk.resources;

/**
 * 资源类型
 * 
 * @author 792793182@qq.com 2014-1-15
 *
 */
public enum ResourceType {

	/** 显示资源 */   drawable, 
	/** 颜色资源 */   color,
	/** 样式资源 */   style,
	/** 尺寸资源 */   dimen,
	/** 字符串   */  string,
	/** xml资源 */   xml,
	/** 普通动画 */   anim,
	/** 属性动画 */   animator,
	/** 菜单    */   menu
}
